<h1>CTP</h1>

<p>Nasza drukarnia posiada własne studio DTP wraz z naświetlarnią CtP.</p> 
<p>Operujemy nowoczesnym systemem CtP, dzięki czemu mają Państwo pewność, że wydruki u nas będą zawsze najwyższej jakości.</p> 
 
<br/> 
<h3>Jak przygotować pracę do druku?</h3>
<p>Przyjmujemy co prawda prace w niemal każdym formacie ale poprawnie przygotowana praca powinna być zapisana w formacie PDF . W przypadku tła w pracy konieczne jest dodanie tzw. spadów <br />(ok. 2 - 3mm) czyli wyciągnięciu tła poza rozmiary dokumentu. Czcionka załączona w pliku musi być zamieniona na krzywe.</p>
<br/>
<h4>Praca do druku:</h4>
<ul>
	<li>format pliku PDF</li>
    <li>Czcionka załączona w pliku lub jako krzywe</li>
    <li>spady jeśli elementy dochodzą do krawędzi pracy</li>
</ul>
<br/>
<p>W przypadku zeszytów szytych zaleca się zadawanie większych spadów (szczególnie dla okładki), nawet do 5 mm.</p> 


 
