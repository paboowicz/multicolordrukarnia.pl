<h1>INICIO</h1>
<p><b>Acerca de nosotros</b></p> 
<br/>
<p>Estamos en el mercado desde 2004. Elevamos sistemáticamente nuestras calificaciones e invertimos en los equipos de los principales fabricantes de la industria de la impresión. Ofrecemos servicios en el campo de la impresión offset, impresión digital e impresión en toda la gama de encuadernación y diseño gráfico. Para no defraudar la confianza depositada en nosotros, hemos desarrollado un estricto control de calidad. Gracias a ello, todo el proceso de producción está bajo constante control.</p>
<br/>
<h2>Qué nos distingue?</h2>
<ul>
<li>calidad</li>
<li>una amplia gama de servicios</li>
<li>reducidos plazos de entrega</li>
<li>puntualidad</li>
<li>atención personalizada</li>
<li>servicio amable y profesional</li>
</ul>
 <br/>
<h4>Máquinas de impresión en impresión multicolor:</h4>
<ul>
<li>Man Roland 700 Evolution  (offset press)
<li>Shinohara 75 V HP (offset press)
<li>Sakurai  575 + coating (offset press)
<li>Mark Andy 10 colors (flexo press)
<li>Lombardi 7 colors (flexo press)
</ul>

<br/>

<p>También tenemos un parque bien equipado de máquinas de encuadernación.</p>
 
<p>La imprenta tiene una línea tecnológica completa que va desde el almacén de computadoras hasta la preimpresión, imprimiendo en la encuadernación.</p> 

<ul>
<li>El tiempo necesario para iniciar el pedido se cuenta en minutos. La impresión offset se realiza en formato B1+. Imprimimos digitalmente en formato A3.</li> 
<li>Los dispositivos separados para la impresión digital en color en blanco y negro permiten obtener los precios más bajos en el mercado de este tipo de servicios.</li>
<li>La implementación en nuestros trabajos, de la tecnología de impresión offset y digital nos permite trabajar con impresiones muy pequeñas y muy grandes de cualquier tipo.</li> 
</ul>


